* **الجا (θ) = المقابل ÷ الوتر**

### 2. جيب التمام (Cos - جتا)
النسبة بين طول الضلع المجاور وطول الوتر.
* **الجتا (θ) = المجاور ÷ الوتر**

### 3. الظل (Tan - ظا)
النسبة بين طول الضلع المقابل وطول الضلع المجاور.
* **الظا (θ) = المقابل ÷ المجاور** *(وهي أيضاً تساوي الجا ÷ الجتا)*

