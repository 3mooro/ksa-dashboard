## المفهوم الأول: النهايات (Limits)

لا يمكن فهم التفاضل دون فهم **النهايات**. النهاية تعني دراسة سلوك دالة رياضية عندما تقترب قيمة المتغير (س) من رقم معين، ولكن دون الوصول إليه تماماً.

* **مثال بسيط:** إذا اقتربت من جدار، في كل خطوة تقطع نصف المسافة المتبقية. من الناحية العملية، لن تلمس الجدار أبداً، ولكن النهاية (Limit) تشير إلى أنك "ستصل إلى الجدار".
* **أهميتها:** تُستخدم النهايات للتغلب على المشكلات الرياضية مثل القسمة على صفر، وهي حجر الأساس لاشتقاق الدوال.

