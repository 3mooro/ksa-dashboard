## المفهوم الثاني: التفاضل والاشتقاق (Derivatives)

التفاضل هو عملية إيجاد "المشتقة". المشتقة بكل بساطة هي مقياس لمعدل التغير اللحظي.

### مثال واقعي للاشتقاق
إذا كانت الدالة الرياضية تمثل "المسافة" التي تقطعها سيارة مع مرور "الزمن"، فإن **المشتقة الأولى** لهذه الدالة هي "السرعة"، و**المشتقة الثانية** هي "التسارع".

### القاعدة الذهبية للاشتقاق (قاعدة القوة)
إذا كانت الدالة: ص = س^ن
فإن المشتقة (ص'') = ن × س^(ن-1)
> مثال: مشتقة الدالة (س³) هي (3س²)

