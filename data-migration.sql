## ما هو الجبر؟

الجبر هو فرع من فروع الرياضيات يهتم بدراسة الرموز الرياضية والقواعد لمعالجة هذه الرموز. بدلاً من استخدام الأرقام فقط كما في الحساب التقليدي، نستخدم في الجبر حروفاً مثل (س، ص) أو (x, y) لتمثيل قيم مجهولة.

